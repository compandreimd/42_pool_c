#include <unistd.h>

int		ft_putchar(char c);

void	ft_print_comb2(void)
{
	int a;
	int b;

	a = 0;
	while (a <= 99)
	{
		b = a + 1;
		while (b <= 99)
		{
			ft_putchar('0' + a / 10);
			ft_putchar('0' + a % 10);
			ft_putchar(' ');
			ft_putchar('0' + b / 10);
			ft_putchar('0' + b % 10);
			b++;
			if (a < 99 & b < 99)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
		}
		a++;
	}
}
